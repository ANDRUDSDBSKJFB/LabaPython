# Инициализация переменных
total_sum = 0
count = 0

# Ввод последовательности чисел
numbers = []
print("Введите целые числа (окончите ввод нулем):")
while True:
    num = int(input())
    if num == 0:
        break
    numbers.append(num)

# Используем цикл for для подсчета суммы и количества
for number in numbers:
    total_sum += number
    count += 1

# Вывод результатов
print("Сумма всех чисел последовательности:", total_sum)
print("Количество всех чисел последовательности:", count)

# Ввод числа N
N = int(input("Введите целое число N (> 1): "))

# Инициализация переменной K
K = 0

# Используем цикл while для поиска K
while 2 ** K <= N:
    K += 1

# Вывод результата
print("Наибольшее целое число K, при котором 2^K > N:", K)
